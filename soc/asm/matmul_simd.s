start:
    # Jump to matrix multiplication routine
    jal mat_mul_simd

#------------------------------------------------------------
# mat_mul_simd:
#
#   Compute:  C[i][j] += Sum_k ( A[i][k] * B[k][j] )
#
# Matrices are 32x32. The loops are organized as:
#
#   for (i = 0; i < M; i++) {
#     for (j_block = 0; j_block < N; j_block += 4) {
#       acc_lo = 0; acc_hi = 0;
#       for (k = 0; k < K; k++) {
#         // Broadcast A[i][k] into t3 = {A, A, A, A}
#         // Load 4 bytes from B[k][j_block] into t4.
#         // Multiply:
#         //    spmuli8i16s.vv.l t5, t3, t4 → lower 2 16-bit products
#         //    spmuli8i16s.vv.h t6, t3, t4 → upper 2 16-bit products
#         // Accumulate:
#         //    acc_lo += t5;   acc_hi += t6.
#       }
#       // After k loop, narrow acc_lo and acc_hi using samuli8i8s.vv.l
#       // then pack the results and store 4 bytes into C[i][j_block...j_block+3].
#     }
#   }
#
# Register assignment (physical register numbers in parentheses):
#   s0 = x8  : A base address
#   s1 = x9  : B base address
#   s2 = x18 : C base address
#   s3 = x19 : M (32)
#   s4 = x20 : K (32)
#   s5 = x21 : N (32)
#
#   t0 = x5  : i index (row of A/C)
#   t1 = x6  : j_block index (columns in blocks of 4)
#   t2 = x7  : k index (inner dimension)
#
#   t3 = x28 : temporary for broadcasting A element
#   t4 = x29 : temporary for loaded B block
#   t5 = x30 : result from spmuli8i16s.vv.l (lower half)
#   t6 = x31 : result from spmuli8i16s.vv.h (upper half)
#
#   a2 = x12 : accumulator for lower two lanes (16-bit each)
#   a3 = x13 : accumulator for upper two lanes (16-bit each)
#
#   a4 = x14 : temporary used during narrowing/packing
#   a5 = x15 : temporary used during narrowing/packing
#   a6 = x16 : constant 0x01010101 for narrowing (identity)
#
#------------------------------------------------------------
mat_mul_simd:
    # Set up base addresses and dimensions.
    la    s0, A          
    # A base address (x8)
    la    s1, B         
    # B base address (x9)
    la    s2, C          
    # C base address (x18)
    li    s3, 32         
    # M = 32 (x19)
    li    s4, 32         
    # K = 32 (x20)
    li    s5, 32         
    # N = 32 (x21)

    li    t0, 0          
    # i = 0 (x5)
loop_i:
    bge   t0, s3, finish_matmul  
    # if (i >= M) then done

    li    t1, 0          
    # j_block = 0 (x6)
loop_j_block:
    bge   t1, s5, next_i         
    # if (j_block >= N) then next row

    # Initialize accumulators for this 4-element block.
    li    a2, 0         
    # acc_lo = 0 (x12)
    li    a3, 0         
    # acc_hi = 0 (x13)

    li    t2, 0         
    # k = 0 (x7)
loop_k:
    bge   t2, s4, store_block   
    # if (k >= K) then store results

    # --- Compute address for A[i][k] ---
    mul   t3, t0, s4    
    # t3 = i * 32 (x28)
    add   t3, t3, t2    
    # t3 = i*32 + k
    add   t3, s0, t3    
    # t3 = address of A[i][k]
    lb    t4, 0(t3)     
    # t4 = A[i][k]

    # --- Manually broadcast A[i][k] into t3 ---
    andi  t4, t4, 0xFF  
    # ensure 8-bit value in t4
    mv    t3, t4       
    # move value into t3
    slli  t3, t3, 8    
    # shift left 8 bits
    or    t3, t3, t4   
    # lower 16 bits = {A, A}
    slli  t3, t3, 8    
    # shift left another 8 bits
    or    t3, t3, t4   
    # lower 24 bits = {A, A, A}
    slli  t3, t3, 8    
    # shift left another 8 bits
    or    t3, t3, t4   
    # t3 = {A, A, A, A}

    # --- Load 4 bytes from B[k][j_block] ---
    mul   t4, t2, s5    
    # t4 = k * 32
    add   t4, t4, t1    
    # t4 = k*32 + j_block
    add   t4, s1, t4    
    # t4 = address of B[k][j_block]
    lw    t4, 0(t4)     
    # t4 now holds 4 bytes of B (packed)

    # --- SIMD multiply ---
    spmuli8i16s.vv.l t5, t3, t4   
    # lower two 16-bit products in t5
    spmuli8i16s.vv.h t6, t3, t4   
    # upper two 16-bit products in t6

    # --- Accumulate into accumulators ---
    saddi16i16s.vv a2, a2, t5

    saddi16i16s.vv a3, a3, t6

    addi  t2, t2, 1            
    # k++
    j     loop_k

store_block:
    # Manual narrowing and packing
    andi  a4, a2, 0xFF           
    # byte0
    srli  t6, a2, 16
    andi  t6, t6, 0xFF           
    # byte1
    slli  t6, t6, 8
    or    a4, a4, t6

    andi  t6, a3, 0xFF           
    # byte2
    slli  t6, t6, 16
    or    a4, a4, t6

    srli  t6, a3, 16
    andi  t6, t6, 0xFF           
    # byte3
    slli  t6, t6, 24
    or    a4, a4, t6

    # Store 4 packed bytes to C[i][j_block..j_block+3]
    mul   t4, t0, s5
    add   t4, t4, t1
    add   t4, s2, t4
    sw    a4, 0(t4)

    addi  t1, t1, 4
    j     loop_j_block

next_i:
    addi  t0, t0, 1          
    # next row (i++)
    j     loop_i

finish_matmul:
    epilogue

epilogue:
    # DMA copy C -> dest_data
    li   x1, 0x0000f000       
    # DMA MMIO base
    la   x2, C                
    # Source: matrix C
    li   x3, 0x3000        
    # Destination: buffer
    li   x4, 0x20201e1e
    # TW=2, TH=2 → 9 bytes

    li   x5, 1                
    # ENABLE
    sw   x2, 4(x1)            
    # SOURCE_INFO
    sw   x3, 8(x1)            
    # DEST_INFO
    sw   x4, 12(x1)           
    # DMA_SIZE_CFG
    sb   x5, 0(x1)            
    # Enable DMA

check_done:
    lw   x6, 20(x1)           
    # DONE register
    and  x6, x6, x5
    beq  x6, x0, check_done
    sw   x0, 20(x1)           
    # Clear DONE

readback:
    # (Optional) load values from dest_data
    lw   x16, 0(x3)
    lw   x17, 4(x3)
    lb   x18, 8(x3)

    hcf                      # Halt
