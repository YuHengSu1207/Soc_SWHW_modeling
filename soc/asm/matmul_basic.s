start:
    # Jump to matrix multiplication routine
    jal mat_mul_asm

#------------------------------------------------------------------
# mat_mul_asm: Scalar matrix multiplication using base addressing
#------------------------------------------------------------------
mat_mul_asm:
    # Initialize stack pointer
    li   sp, 0xefff
    # Set up base addresses
    la   s0, A       
    # base of A
    la   s1, B       
    # base of B
    la   s2, C       
    # base of C

    # Matrix dimensions
    li   s3, 32
     # M = 3
    li   s4, 32
    # K = 2
    li   s5, 32
    # N = 3
    li   t0, 0       
    # i = 0
dimM:
    bge  t0, s3, epilogue
    li   t1, 0       
    # j = 0
    jal  dimN
    addi t0, t0, 1
    j    dimM

dimN:
    bge  t1, s5, dimN_end
    addi sp, sp, -4
    sw   ra, 0(sp)
    li   t2, 0       
    # k = 0
    jal  dimK
    lw   ra, 0(sp)
    addi sp, sp, 4
    addi t1, t1, 1
    j    dimN
dimN_end:
    ret

dimK:
    bge  t2, s4, dimK_end
    addi sp, sp, -4
    sw   ra, 0(sp)
    jal  mult
    lw   ra, 0(sp)
    addi sp, sp, 4
    addi t2, t2, 1
    j    dimK
dimK_end:
    ret

mult:
    # A[i][k]
    mul t3, t0, s4
    add t3, t3, t2
    add t3, s0, t3
    lb  t4, 0(t3)

    # B[k][j]
    mul t3, t2, s5
    add t3, t3, t1
    add t3, s1, t3
    lb  t5, 0(t3)

    # Multiply
    mul t6, t4, t5

    # C[i][j] += A[i][k] * B[k][j]
    mul t3, t0, s5
    add t3, t3, t1
    add t3, s2, t3
    lb  t4, 0(t3)
    add t6, t6, t4
    sb  t6, 0(t3)
    ret

epilogue:
    # DMA copy C -> dest_data
    li   x1, 0x0000f000       
    # DMA MMIO base
    la   x2, C                
    # Source: matrix C
    li   x3, 0x3000        
    # Destination: buffer
    li   x4, 0x20201e1e
    # TW=2, TH=2 â†’ 9 bytes

    li   x5, 1                
    # ENABLE
    sw   x2, 4(x1)            
    # SOURCE_INFO
    sw   x3, 8(x1)            
    # DEST_INFO
    sw   x4, 12(x1)           
    # DMA_SIZE_CFG
    sb   x5, 0(x1)            
    # Enable DMA

check_done:
    lw   x6, 20(x1)           
    # DONE register
    and  x6, x6, x5
    beq  x6, x0, check_done
    sw   x0, 20(x1)           
    # Clear DONE

readback:
    # (Optional) load values from dest_data
    lw   x16, 0(x3)
    lw   x17, 4(x3)
    lb   x18, 8(x3)

    hcf                      # Halt
